package study.ch02;

public class IntegerLiteralExample {

    public static void main(String[] arg) {
        byte b = 100;
        short s = 10;
        int age = 25;
        System.out.println(b);      // 100
        System.out.println(s);      // 10
        System.out.println(age);    // 25
    }
}
